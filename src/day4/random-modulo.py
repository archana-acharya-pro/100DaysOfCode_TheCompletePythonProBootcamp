# Randomization & Lists

import random as rd

random_int = rd.randint(0, 10)
print(random_int)

random_float = rd.random()
print(random_float)

random_float_extend_range = rd.random() * 10
print(random_float_extend_range)

random_float1 = rd.uniform(0.0, 5.0)
print(random_float1)
